<div label-module="para" class="para">搬血：需调动全身精血，滚滚如雷鸣，熔炼骨文，在血液中催发出神曦，从而淬炼天地造化，滋养肉身。最高可使肉身达至十万斤极境</div>
<div label-module="para" class="para">洞天：开辟洞天等于是夺了天地造化，不断直接吸收外界神精，补充己身。最多可有十大洞天，石昊将十大洞天合一化为唯一无上洞天，疑似荒天帝修补演化仙域的重要手段</div>
<div label-module="para" class="para">化灵：重塑真我过程，与以往不同的蜕变，从肉身到精神，再到沟通外界的洞天，产生灵性，都将演变</div>
<div label-module="para" class="para">铭纹：不再彻底借鉴凶兽和猛禽的符文，能在体内铭刻自己一些符号</div>
<div label-module="para" class="para">列阵：若说铭纹境是模仿其他种族，在体内刻下符文，能初步推演法，那它就是更高层次进化。能在体内刻下各种杀阵，如先天混沌大阵和曹雨生体内的第三杀阵</div>
<div label-module="para" class="para">尊者（人皇）：平凡修士中的极致，超脱世俗，有成神可能</div>
<div label-module="para" class="para">神火（伪神）：点燃神火，超脱凡人，能在体内种下道种。晚年时神火可能熄灭</div>
<div label-module="para" class="para">真一（真神）：真神灵，拥有超脱世俗的力量。真正点燃神火，神火死后才会熄灭</div>
<div label-module="para" class="para">圣祭：特殊过渡境界，前期法力不稳定，在神火境和天神境之间变动，修炼至后期较稳定。它能直接跳过不修炼，如果修炼日后能顺风顺水。它可度神王劫</div>
<div label-module="para" class="para">天神：能断肢再生，一滴血能崩塌山脉，它威压可盖一切低境界。它是进入帝关战场的最低资格</div>
<div label-module="para" class="para">虚道（教主）：与道交融，以完美种子为媒介，触摸大道，能感悟天地间的妙理，第一次跟道全面接触，甚至给人以错觉，宛若化身成天地大道，触摸道的源头</div>
<div label-module="para" class="para">斩我：全称斩我明道境，斩掉己身，明悟自己的道与法</div>
<div label-module="para" class="para">遁一：统领一方的修士，帝关战场首领人物</div>
<div label-module="para" class="para">至尊：矗立于人道绝巅仅是散发威压便令低境界抬不起头，依靠长生物质能活上数百万年或更久。对应遮天境界中的准帝到大帝极巅（准帝级至尊有金太君，大帝极巅级至尊有孟天正）</div>
<div label-module="para" class="para">真仙：一滴血能保持数个纪元不灭，红尘仙是大段位囊括完美所有仙的境界，但它战力不如红尘仙</div>
